package server;
/**
 * クライアントのアドレスを保存するBean
 * @author kanayama
 *
 */
class ClientLogicBean {
	private String firstLogic;
	private String secondLogic;


	public String getFirstLogic() {
		return this.firstLogic;
	}
	public void setFirstLogic(String firstLogic) {
		this.firstLogic = firstLogic;
	}
	public String getSecondLogic() {
		return this.secondLogic;
	}
	public void setSecondLogic(String secondLogic) {
		this.secondLogic = secondLogic;
	}
}
